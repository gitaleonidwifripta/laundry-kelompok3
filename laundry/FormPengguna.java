    private void tbpenggunaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbpenggunaMouseClicked
        // TODO add your handling code here:
        int baris = tbpengguna.rowAtPoint(evt.getPoint());
        String id = tbpengguna.getValueAt(baris, 0).toString();
        txtidpgn.setText(id);
        String nama = tbpengguna.getValueAt(baris, 1).toString();
        txtnamakar.setText(nama);
        String username = tbpengguna.getValueAt(baris, 2).toString();
        txtusername.setText(username);
        String password = tbpengguna.getValueAt(baris, 3).toString();
        txtpassowrd.setText(password);
        String status = tbpengguna.getValueAt(baris, 4).toString();
        txtstatus.setText(status);
    }//GEN-LAST:event_tbpenggunaMouseClicked

