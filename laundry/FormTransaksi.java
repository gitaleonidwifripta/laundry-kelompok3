     public void datable(){
        
        DefaultTableModel tbl=new DefaultTableModel();
        tbl.addColumn("Id Transaksi");
        tbl.addColumn("Nama ");
        tbl.addColumn("Tanggal");
        tbl.addColumn("jenis Cucian");
        tbl.addColumn("Harga");
        tbl.addColumn("Berat");
        tbl.addColumn("Total");
        tbl.addColumn("Bayar");
        tbl.addColumn("Kembalian");
        
        tbpembayaran.setModel(tbl);
        try{
            Statement statement=(Statement) connect.GetConnection().createStatement();
            ResultSet res=statement.executeQuery("select * from tb_transaksi");
            while(res.next())
            {
                tbl.addRow(new Object[]{            
                res.getString("id_transaksi"),
                res.getString("id_pelanggan"),
                res.getString("tanggal_transaksi"),
                res.getString("id_jeniscucian"),
                res.getString("harga"),
                res.getString("berat"),
                res.getString("total_harga"),
                res.getString("dibayar"),
                res.getString("kembalian")
                
            });
            tbpembayaran.setModel(tbl);
            }

        }catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, "salah");
        
        }
     }
 public void dataFromDataBaseToComboBox(){
        try {
            String query = "SELECT * FROM tb_pelanggan";
            Statement st = (Statement) db.GetConnection().createStatement();
            ResultSet rs = st.executeQuery(query);
            
            while (rs.next()) {                
                cmbidplgn.addItem(rs.getString("id_pelanggan"));
                
            }
            
            rs.last();
            int jumlahdata = rs.getRow();
            rs.first();
            
        } catch (SQLException e) {
        }
 }
 
