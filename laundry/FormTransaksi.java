    private void btncariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncariActionPerformed
        // TODO add your handling code here:
          try{
            Statement statement = (Statement) connect.GetConnection().createStatement();
            ResultSet res = statement.executeQuery("select * from tb_transaksi where " + "id_transaksi='" + txtidtran.getText() + "'");
            DefaultTableModel tbl = new DefaultTableModel();
            tbl.addColumn("Id Transaksi");
            tbl.addColumn("Nama ");
            tbl.addColumn("Tanggal");
            tbl.addColumn("jenis Cucian");
            tbl.addColumn("Harga");
            tbl.addColumn("Berat");
            tbl.addColumn("Total");
            tbl.addColumn("Bayar");
            tbl.addColumn("Kembalian");
            tbpembayaran.setModel(tbl);
            
            while(res.next()){
                tbl.addRow(new Object[]{
                res.getString("id_transaksi"),
                res.getString("id_pelanggan"),
                res.getString("tanggal_transaksi"),
                res.getString("id_jeniscucian"),
                res.getString("harga"),
                res.getString("berat"),
                res.getString("total_harga"),
                res.getString("dibayar"),
                res.getString("kembalian")
                });
                tbpembayaran.setModel(tbl);
            }
        }catch(Exception t){
            JOptionPane.showMessageDialog(rootPane,"salah");
        }
            
       
    }//GEN-LAST:event_btncariActionPerformed

